export function DataTable<Tdata>({
  data: initialData,
  columns,
  reportTitle,
  companyName,
}: {
  data: Tdata []
  columns: ColumnDef<Tdata>[]
  reportTitle: string
  companyName: string
}) {
  const [data, setData] = React.useState(() => initialData)
  const [rowSelection, setRowSelection] = React.useState({})
  const [columnVisibility, setColumnVisibility] =
    React.useState<VisibilityState>({})
  const [columnFilters, setColumnFilters] = React.useState<ColumnFiltersState>(
    []
  )
  const [sorting, setSorting] = React.useState<SortingState>([])
  const [pagination, setPagination] = React.useState({
    pageIndex: 0,
    pageSize: 10,
  })

  const dataIds = React.useMemo<UniqueIdentifier[]>(
    () => data?.map(({ id }) => id) || [],
    [data]
  )

  const { user } = useUser()

  const table = useReactTable({
    data,
    columns,
    state: {
      sorting,
      columnVisibility,
      rowSelection,
      columnFilters,
      pagination,
    },
    getRowId: (row) => row.id.toString(),
    enableRowSelection: true,
    onRowSelectionChange: setRowSelection,
    onSortingChange: setSorting,
    onColumnFiltersChange: setColumnFilters,
    onColumnVisibilityChange: setColumnVisibility,
    onPaginationChange: setPagination,
    getCoreRowModel: getCoreRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getSortedRowModel: getSortedRowModel(),
    getFacetedRowModel: getFacetedRowModel(),
    getFacetedUniqueValues: getFacetedUniqueValues(),
  })

  function handleDragEnd(event: DragEndEvent) {
    const { active, over } = event
    if (active && over && active.id !== over.id) {
      setData((data) => {
        const oldIndex = dataIds.indexOf(active.id)
        const newIndex = dataIds.indexOf(over.id)
        return arrayMove(data, oldIndex, newIndex)
      })
    }
  }
  
  function exportTableToPDF() {
    const doc = new jsPDF({
      orientation: "landscape",
      unit: "pt",
      format: "a4",
    })

    const pageWidth = doc.internal.pageSize.getWidth()
    const title = reportTitle ?? "Report"
    const exportedBy = user?.fullName ?? user?.primaryEmailAddress?.emailAddress ?? "Unknown User"    
    // -------- TITLE --------
    doc.setFontSize(18)
    doc.text(title, pageWidth / 2, 40, { align: "center" })

    doc.setFontSize(10)
    doc.text(exportedBy, pageWidth / 2, 20, { align: "center"})

    doc.setFontSize(10)
    doc.text(
      `Exported on ${new Date().toLocaleDateString()} ${new Date().toLocaleTimeString()}`,
      pageWidth / 2,
      40,
      { align: "center" }
    )

    // -------- TABLE DATA --------
    const visibleColumns = table
      .getAllLeafColumns()
      .filter(
        (col) => col.getIsVisible() && col.id !== "actions"
      )

    const headers = visibleColumns.map((col) =>
      typeof col.columnDef.header === "string"
        ? col.columnDef.header
        : col.id
    )

    const rows = table.getFilteredRowModel().rows.map((row) =>
      visibleColumns.map((col) => {
        const value = row.getValue(col.id)

        // Clean objects / JSX
        if (typeof value === "object") {
          return JSON.stringify(value)
        }

        return value ?? ""
      })
    )

    autoTable(doc, {
      startY: 80,
      head: [headers],
      body: rows,
      theme: "striped",
      styles: {
        fontSize: 9,
        cellPadding: 6,
      },
      headStyles: {
        fillColor: [30, 41, 59], // dark slate
        textColor: 255,
        fontStyle: "bold",
      },
      alternateRowStyles: {
        fillColor: [245, 245, 245],
      },
      margin: { left: 40, right: 40 },
      didDrawPage: function (data) {
        const pageCount = doc.getNumberOfPages()
        const pageSize = doc.internal.pageSize
        const pageHeight = pageSize.getHeight()

        doc.setFontSize(9)
        doc.text(
          `Page ${doc.getCurrentPageInfo().pageNumber} of ${pageCount}`,
          pageWidth - 40,
          pageHeight - 10,
          { align: "right" }
        )
      },
    })

    // -------- FILE NAME --------
    const fileName = `laporan-${new Date()
      .toISOString()
      .split("T")[0]}.pdf`

    doc.save(fileName)
  }

  return (
    <div>
      <div className="flex flex-row py-4">
        <div className="basis-1/3">
          <Input
            placeholder="Filter emails..."
            className="max-w-sm"
          />
        </div>
        <div className="basis-2/3 flex justify-end">
          <Button onClick={exportTableToPDF}>
              Export PDF
          </Button>
        </div>
      </div>
      <div className="overflow-hidden rounded-md border">
        <Table>
          <TableHeader className="bg-muted sticky top-0 z-10">
            {table.getHeaderGroups().map((headerGroup) => (
              <TableRow key={headerGroup.id}>
                {headerGroup.headers.map((header) => {
                  return (
                    <TableHead key={header.id} colSpan={header.colSpan}>
                      {header.isPlaceholder
                        ? null
                        : flexRender(
                          header.column.columnDef.header,
                          header.getContext()
                        )}
                    </TableHead>
                  )
                })}
              </TableRow>
            ))}
          </TableHeader>
          <TableBody className="**:data-[slot=table-cell]:first:w-8">
            {table.getRowModel().rows?.length ? (
              <SortableContext
                items={dataIds}
                strategy={verticalListSortingStrategy}
              >
                {table.getRowModel().rows.map((row) => (
                  <DraggableRow key={row.id} row={row} />
                ))}
              </SortableContext>
            ) : (
              <TableRow>
                <TableCell
                  colSpan={columns.length}
                  className="h-24 text-center"
                >
                  No results.
                </TableCell>
              </TableRow>
            )}
          </TableBody>
        </Table>
        <DataTablePagination table={table}/>
      </div>
    </div>
  )
}

